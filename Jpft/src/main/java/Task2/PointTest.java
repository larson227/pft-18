package Task2;

public class PointTest {
    private double p1;
    private double p2;
    private double x1;
    private double x2;
    private double y1;
    private double y2;

    public PointTest(double p1, double p2, double x1, double x2, double y1, double y2) {
        this.p1 = p1;
        this.p2 = p2;
        this.x1 = x1;
        this.x2 = x2;
        this.y1 = y1;
        this.y2 = y2;
    }

    public double getP1() {
        return p1;
    }

    public double getP2() {
        return p2;
    }

    public double getX1() {
        return x1;
    }

    public double getX2() {
        return x2;
    }

    public double getY1() {
        return y1;
    }

    public double getY2() {
        return y2;
    }


}
